// # Sejarah Go / Go-Lang
// Dibuat di Google menggunakan bahasa pemograman C
// Dirilis ke publik sebagai open source pada tahun 2009
// Go-Lang populer sejak digunakan untuk membuat Docker pada tahun 2011
// Saat ini mulai banyak teknologi baru yang dibuat menggunakan bahasa Go-Lang dibanding bahasa C, seperti Kubernetes, Prometheus, CockroachDB, dan lain-lain
// Saat ini mulai populer untuk pembuatan Backend API di Microservices


// # Kenapa Belajar Go-Lang?
// Berbeda hal nya dengan bahasa pemograman lain semisal (Java, C++, Javascript, dll) yang mengadaptasi banyak konsep seperti Object Oriented dan lain-lain, Bahasa Go / Go-Lang sangatlah sederhana, tidak butuh waktu lama untuk mempelajarinya
// Go-Lang mendukung concurrency programming, dimana saat ini kita hidup di zaman multi-core processor
// Go-Lang mendukung garbage collector, sehingga tidak butuh melakukan managemenet memori secara manual seperti pada bahasa C
// garbage collector : secara sederhananya adalah data-data yang sudah tidak dibutuhkan dalam aplikasi secara otomatis bisa dihapus oleh Go-Lang
// Salah satu bahasa pemograman yang sedang naik daun saat ini


// # Proses Development Go-Lang / Cara membuat aplikasi di Go-Lang
// 1. Instal Go Compiler -> untuk melakukan kompilasi kode program Go-Lang menjadi bahasa mesin
// 2. Tulis kode program Go-Lang menggunakan text editor / IDE dengan ekstensi .go
// 3. Kode program yang sudah ditulis selanjutnya akan di kompilasi menggunakan Go Compiler sehingga dengan begitu akan menghasilkan file binary yang bisa langsung dijalankan di sistem operasi
